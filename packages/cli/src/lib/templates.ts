/**
 * Generate a discovery report template.
 */
export function discoveryReportTemplate(
  client: string,
  projectType: ProjectType,
  tools: string[],
  timeline: string,
  budget: string,
  scores: { category: string; score: number; notes: string }[]
): string {
  const date = new Date().toISOString().split('T')[0];

  const fm = frontmatter({
    title: `${client} — Discovery Report`,
    client,
    type: projectType,
    date,
    version: '1.0.0',
  });

  const avgScore = scores.length > 0
    ? (scores.reduce((sum, s) => sum + s.score, 0) / scores.length).toFixed(1)
    : '0';

  const body = [
    heading(`${client} — Discovery Report`, 1),
    heading('Executive Summary', 2),
    `Discovery session completed for ${client}. Overall readiness score: **${avgScore}/10**.\n`,
    heading('Client Profile', 2),
    `- **Client:** ${client}`,
    `- **Project Type:** ${projectType}`,
    `- **Timeline:** ${timeline}`,
    `- **Budget Range:** ${budget}\n`,
    heading('Scoring Matrix', 2),
    table(
      ['Category', 'Score (1-10)', 'Notes'],
      scores.map((s) => [s.category, String(s.score), s.notes])
    ),
    heading('Tools & Technologies', 2),
    tools.length > 0 ? tools.map((t) => `- ${t}`).join('\n') + '\n' : '> To be determined.\n',
    heading('Recommendations', 2),
    checklist([
      'Confirm licensing requirements with Microsoft partner',
      'Set up development environments early',
      'Establish governance framework before build',
      'Plan for iterative user feedback cycles',
    ]),
    heading('Identified Risks', 2),
    checklist([
      'Scope not fully defined — needs follow-up workshop',
      'Integration points require technical validation',
      'Change management may impact timeline',
    ]),
    heading('Next Steps', 2),
    '1. Finalize project scope document\n2. Generate PRD\n3. Begin solution design phase\n4. Provision environments\n',
    horizontalRule(),
    '_Generated by pp-agency_\n',
  ].join('\n');

  return fm + body;
}

/**
 * Generate a licensing estimate document.
 */
export function licensingEstimateTemplate(
  currency: string,
  components: { name: string; quantity: number; unitPrice: number; total: number }[],
  monthlyTotal: number,
  annualTotal: number
): string {
  const date = new Date().toISOString().split('T')[0];
  const currencySymbol = currency === 'gbp' ? '£' : currency === 'eur' ? '€' : '$';

  const fm = frontmatter({
    title: 'Power Platform Licensing Estimate',
    date,
    currency,
    version: '1.0.0',
  });

  const body = [
    heading('Power Platform Licensing Estimate', 1),
    heading('Component Breakdown', 2),
    table(
      ['Component', 'Quantity', `Unit Price (${currencySymbol})`, `Total (${currencySymbol})`],
      components.map((c) => [
        c.name,
        String(c.quantity),
        `${currencySymbol}${c.unitPrice.toFixed(2)}`,
        `${currencySymbol}${c.total.toFixed(2)}`,
      ])
    ),
    heading('Summary', 2),
    `- **Monthly Total:** ${currencySymbol}${monthlyTotal.toFixed(2)}`,
    `- **Annual Total:** ${currencySymbol}${annualTotal.toFixed(2)}\n`,
    heading('Notes', 2),
    '> Prices are estimates based on current Microsoft pricing. Actual costs may vary based on',
    '> region, volume discounts, and licensing agreements. Contact a Microsoft partner for',
    '> formal pricing.\n',
    heading('Recommendations', 2),
    checklist([
      'Review licensing with Microsoft partner for volume discounts',
      'Consider Power Apps per-app plans for infrequent users',
      'Evaluate pay-as-you-go for variable workloads',
      'Plan for seasonal usage spikes',
    ]),
    horizontalRule(),
    '_Generated by pp-agency_\n',
  ].join('\n');

  return fm + body;
}
