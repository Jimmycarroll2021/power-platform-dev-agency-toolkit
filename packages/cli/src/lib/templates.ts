/**
 * Template engine for generating Power Platform project documents.
 *
 * Templates use {{variableName}} syntax for variable substitution.
 * Built-in templates are provided for all standard document types.
 */

import {
  type ProjectConfig,
  AgentType,
  ProjectType,
} from './project-types.js';
import {
  frontmatter,
  heading,
  table,
  codeBlock,
  checklist,
  horizontalRule,
  bold,
} from './markdown-utils.js';

// ---------------------------------------------------------------------------
// Template engine
// ---------------------------------------------------------------------------

/**
 * Replace {{key}} placeholders in a template string with values from `data`.
 * Keys that are not found in `data` are left untouched.
 */
export function renderTemplate(template: string, data: Record<string, unknown>): string {
  return template.replace(/\{\{(\w+)\}\}/g, (_match, key: string) => {
    if (key in data && data[key] !== undefined && data[key] !== null) {
      return String(data[key]);
    }
    return `{{${key}}}`;
  });
}

// ---------------------------------------------------------------------------
// Template data builders
// ---------------------------------------------------------------------------

/**
 * Build the common variable map used by most templates from a ProjectConfig.
 */
export function buildTemplateData(config: ProjectConfig): Record<string, unknown> {
  return {
    projectName: config.name,
    projectType: config.type,
    projectTypeLabel: getProjectTypeLabel(config.type),
    client: config.client,
    description: config.description,
    createdAt: config.createdAt,
    version: config.version,
    date: new Date().toISOString().split('T')[0],
    year: new Date().getFullYear(),
  };
}

/**
 * Get a human-readable label for a project type.
 */
export function getProjectTypeLabel(type: ProjectType): string {
  const labels: Record<ProjectType, string> = {
    [ProjectType.CanvasApp]: 'Canvas App',
    [ProjectType.ModelDriven]: 'Model-Driven App',
    [ProjectType.PowerPages]: 'Power Pages',
    [ProjectType.Integration]: 'Integration',
    [ProjectType.Copilot]: 'Copilot / AI',
    [ProjectType.FullSolution]: 'Full Solution',
  };
  return labels[type] ?? 'Unknown';
}

/**
 * Get a human-readable label for an agent type.
 */
export function getAgentTypeLabel(type: AgentType): string {
  const labels: Record<AgentType, string> = {
    [AgentType.Architect]: 'Solution Architect',
    [AgentType.DataModeler]: 'Data Modeler',
    [AgentType.Developer]: 'Power Platform Developer',
    [AgentType.Tester]: 'QA / Test Engineer',
    [AgentType.AlmEngineer]: 'ALM Engineer',
    [AgentType.SecurityAdmin]: 'Security Administrator',
  };
  return labels[type] ?? 'Unknown Agent';
}

