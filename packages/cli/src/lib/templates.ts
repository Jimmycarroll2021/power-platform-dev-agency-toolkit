/**
 * Generate a checklist document.
 */
export function checklistTemplate(
  title: string,
  items: { category: string; tasks: string[] }[]
): string {
  const date = new Date().toISOString().split('T')[0];

  const fm = frontmatter({
    title: `${title} Checklist`,
    date,
    version: '1.0.0',
  });

  const bodyParts: string[] = [heading(`${title} Checklist`, 1)];

  for (const section of items) {
    bodyParts.push(heading(section.category, 2));
    bodyParts.push(checklist(section.tasks));
  }

  bodyParts.push(horizontalRule());
  bodyParts.push('_Generated by pp-agency_\n');

  return fm + bodyParts.join('\n');
}

