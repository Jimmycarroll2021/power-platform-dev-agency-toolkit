// ---------------------------------------------------------------------------
// Filesystem round-trip: write a generated doc to a temp dir, then clean up.
// ---------------------------------------------------------------------------

test('generated documents survive a filesystem write/read round-trip', () => {
  const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'pp-agency-templates-'));
  try {
    const cases: Array<[string, string]> = [
      ['project-brief.md', projectBriefTemplate(config)],
      ['prd.md', prdTemplate(config)],
      ['solution-design.md', solutionDesignTemplate(config)],
      ['architect-brief.md', agentBriefTemplate(config, AgentType.Architect)],
    ];

    for (const [fileName, contents] of cases) {
      const filePath = path.join(tmpDir, fileName);
      fs.writeFileSync(filePath, contents, 'utf8');
      const readBack = fs.readFileSync(filePath, 'utf8');
      assert.equal(readBack, contents, `${fileName} round-trips unchanged`);
      assert.ok(readBack.includes(PROJECT_NAME), `${fileName} retains the project name`);
    }
  } finally {
    // Always clean up the temp directory, even if an assertion above fails.
    fs.rmSync(tmpDir, { recursive: true, force: true });
  }
});
