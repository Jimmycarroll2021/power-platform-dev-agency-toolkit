const PROJECT_NAME = 'Contoso CRM';
const CLIENT_NAME = 'Contoso Inc.';

/** A realistic, fully-populated ProjectConfig used across the suite. */
const config: ProjectConfig = {
  name: PROJECT_NAME,
  type: ProjectType.ModelDriven,
  client: CLIENT_NAME,
  description: 'A model-driven CRM for sales operations.',
  createdAt: '2026-01-15',
  version: '1.0.0',
};

/**
 * Assert that a generated document is a non-empty markdown string that carries
 * the project + client name into both the frontmatter and the body footer.
 */
function assertProjectDoc(doc: string): void {
  assert.equal(typeof doc, 'string');
  assert.ok(doc.length > 0, 'document should not be empty');
  // Frontmatter block is present and opens the document.
  assert.ok(doc.startsWith('---\n'), 'document should start with frontmatter');
  // Injected project + client names appear in the rendered output.
  assert.ok(doc.includes(PROJECT_NAME), 'document should contain the project name');
  assert.ok(doc.includes(CLIENT_NAME), 'document should contain the client name');
  // Shared footer marker emitted by the template builders.
  assert.ok(doc.includes('_Generated by pp-agency'), 'document should contain the footer');
}

